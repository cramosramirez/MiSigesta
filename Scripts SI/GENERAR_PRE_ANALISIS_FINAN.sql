ALTER PROCEDURE [dbo].[GENERAR_PRE_ANALISIS_FINAN]
	@ID_ZAFRA_INI INT,
	@ID_ZAFRA_FIN INT,
	@CODIPROVEEDOR VARCHAR(50),
	@CONTRATOS VARCHAR(8000),
	@UID_REF VARCHAR(3000),
	@FECHA DATETIME,
	@USUARIO_CREA VARCHAR(100),
	@MONTO_SOLICITADO NUMERIC(20,2),
	@MONTO_SOLICITADO2 NUMERIC(20,2),
	@TIPO_MONTO_SOLICITADO VARCHAR(50), -- 'OIP', 'ANTICIPO',
	@UID_DOCUMENTO VARCHAR(300)
AS

BEGIN
	--DECLARE 
	--	@ID_ZAFRA_INI INT,
	--	@ID_ZAFRA_FIN INT,
	--	@CODIPROVEEDOR VARCHAR(50),
	--	@CONTRATOS VARCHAR(3000),
	--	@UID_REF VARCHAR(50),
	--	@FECHA DATETIME,
	--	@ID_DETA INT,
	--	@MONTO NUMERIC(20,4),		
	--	@USUARIO_CREA VARCHAR(100),
	--	@MONTO_SOLICITADO NUMERIC(20,2),
	--	@MONTO_SOLICITADO2 NUMERIC(20,2),
	--	@TIPO_MONTO_SOLICITADO VARCHAR(50),
	--	@UID_DOCUMENTO VARCHAR(50),
	--	@UID VARCHAR(300)
	--SET @ID_ZAFRA_INI = 10
	--SET @ID_ZAFRA_FIN = 15
	--SET @CODIPROVEEDOR = '0140150000'
	--SET @CONTRATOS = '2019/20200634;2020/20210608;'
	--SET @UID_REF = CAST(NEWID() AS VARCHAR(50))
	--SET @FECHA = GETDATE()
	--SET @USUARIO_CREA = 'admin'	
	--SET @MONTO_SOLICITADO = 0
	--SET @MONTO_SOLICITADO2 = 0
	--SET @TIPO_MONTO_SOLICITADO = ''
	--SET @UID_DOCUMENTO = ''
	--SET @UID = ''
	
	DECLARE @TABLA_CONTRATOS TBL_CONTRATOS_ANALIS,
			@UID_GUID AS UNIQUEIDENTIFIER

	set @FECHA = getdate()
	IF @UID_DOCUMENTO <> '' SET @UID_GUID = CAST(@UID_DOCUMENTO AS UNIQUEIDENTIFIER)

	DELETE FROM PRE_ANALISIS_ZAFRA 
		WHERE (SELECT COUNT(1) FROM PRE_ANALISIS_ENCA A WHERE A.ID_ENCA = PRE_ANALISIS_ZAFRA.ID_ENCA AND A.USUARIO_CREA = @USUARIO_CREA AND A.FECHA_CREA < @FECHA) > 0
	DELETE FROM PRE_ANALISIS_TC 
		WHERE (SELECT COUNT(1) FROM PRE_ANALISIS_ENCA A WHERE A.ID_ENCA = PRE_ANALISIS_TC.ID_ENCA AND A.USUARIO_CREA = @USUARIO_CREA AND A.FECHA_CREA < @FECHA) > 0
	DELETE FROM PRE_ANALISIS_DOCTO 
		WHERE (SELECT COUNT(1) FROM PRE_ANALISIS_ENCA A WHERE A.ID_ENCA = PRE_ANALISIS_DOCTO.ID_ENCA AND A.USUARIO_CREA = @USUARIO_CREA AND A.FECHA_CREA < @FECHA) > 0
	DELETE FROM PRE_ANALISIS_DETA WHERE USUARIO_CREA = @USUARIO_CREA AND FECHA_CREA < @FECHA
	DELETE FROM PRE_ANALISIS_ENCA WHERE USUARIO_CREA = @USUARIO_CREA AND FECHA_CREA < @FECHA

	
	DECLARE @POS INT,
			@CODICONTRATO VARCHAR(50),
			@NO_CONTRATOS VARCHAR(1000),									
			@ZONAS VARCHAR(1000),
			@TOTAL_MZ NUMERIC(20,4),
			@TOTAL_TC NUMERIC(20,4),
			@TOTAL_MZZ2 NUMERIC(20,4),
			@TOTAL_TCZ2 NUMERIC(20,4),
			@TARIFA_FLETE NUMERIC(20,4),
			@ID_ENCA INT,
			@CODIAGRON1 VARCHAR(8),
			@CODIAGRON2 VARCHAR(8),
			@CODIAGRON3 VARCHAR(8),

			@CUR_CODICONTRATO VARCHAR(1000),
			@CUR_NO_CONTRATO INT,
			@CUR_ZONA VARCHAR(100),			
			
			@@_DESEMBOLSOS_EFECTIVO NUMERIC(20,2),
			@@_SALDOS_INSOLUTOS NUMERIC(20,2),
			@@_OIP1 NUMERIC(20,2),
			@@_OIP2 NUMERIC(20,2),
			@@_OIP1_BANCO VARCHAR(200),
			@@_OIP2_BANCO VARCHAR(200),
			@@_OIP1_UID UNIQUEIDENTIFIER,
			@@_OIP2_UID UNIQUEIDENTIFIER,
			@@_SUMI_FERTILIZANTES NUMERIC(20,2),
			@@_SUMI_HERBICIDAS NUMERIC(20,2),
			@@_SUMI_INSECTICIDAS NUMERIC(20,2),
			@@_SUMI_INHIBIDORES NUMERIC(20,2),
			@@_ORDEN_TRAMITE NUMERIC(20,2),
			@@_OTRAS_CTAS NUMERIC(20,2),		
			@@_CANA_SEMILLA NUMERIC(20,2),
			@@_SERVICIO_MAQUI NUMERIC(20,2),
			@@_FOLIARES NUMERIC(20,2),
			@@_MADURANTES NUMERIC(20,2),
			@@_APLICACION_AEREA NUMERIC(20,2),
			@@_CREDITOS_LARGO_PLAZO NUMERIC(20,2),

			@@_DESEMBOLSOS_EFECTIVOZ2 NUMERIC(20,2),
			@@_SALDOS_INSOLUTOSZ2 NUMERIC(20,2),
			@@_OIP1Z2 NUMERIC(20,2),
			@@_OIP2Z2 NUMERIC(20,2),
			@@_OIP1_BANCOZ2 VARCHAR(200),
			@@_OIP2_BANCOZ2 VARCHAR(200),
			@@_OIP1_UIDZ2 UNIQUEIDENTIFIER,
			@@_OIP2_UIDZ2 UNIQUEIDENTIFIER,
			@@_SUMI_FERTILIZANTESZ2 NUMERIC(20,2),
			@@_SUMI_HERBICIDASZ2 NUMERIC(20,2),
			@@_SUMI_INSECTICIDASZ2 NUMERIC(20,2),
			@@_SUMI_INHIBIDORESZ2 NUMERIC(20,2),
			@@_ORDEN_TRAMITEZ2 NUMERIC(20,2),
			@@_OTRAS_CTASZ2 NUMERIC(20,2),		
			@@_CANA_SEMILLAZ2 NUMERIC(20,2),
			@@_SERVICIO_MAQUIZ2 NUMERIC(20,2),
			@@_FOLIARESZ2 NUMERIC(20,2),
			@@_MADURANTESZ2 NUMERIC(20,2),
			@@_APLICACION_AEREAZ2 NUMERIC(20,2),
			@@_CREDITOS_LARGO_PLAZOZ2 NUMERIC(20,2),

			@TOTAL_MZ_PROPIA NUMERIC(20,4),
			@TOTAL_MZ_PROPIAZ2 NUMERIC(20,4),
			@TOTAL_MZ_ARRENDADA NUMERIC(20,4),
			@TOTAL_MZ_ARRENDADAZ2 NUMERIC(20,4),
			@@ORDEN INT,
			@@MONTO1 NUMERIC(20,10),
			@@MONTO2 NUMERIC(20,10),
			@@MONTO3 NUMERIC(20,10),
			@@MONTO4 NUMERIC(20,10),
			@@MONTO5 NUMERIC(20,10),
			@@MONTO6 NUMERIC(20,10),
			
			@@PROVI_REAL_TC NUMERIC(20,10),
			@@PROVI_REAL_ROZA NUMERIC(20,10),
			@@PROVI_REAL_ALZA NUMERIC(20,10),
			@@PROVI_REAL_FLETE NUMERIC(20,10),
			@@PROVI_REAL_INGRESO NUMERIC(20,10),
			@@PROVI_REAL_EGRESO NUMERIC(20,10),
			@@CODIPROVEE VARCHAR(100),
			@@REND_HISTO NUMERIC(20,10),
			@@ID_ZAFRA_ACTUAL INT,
			@TASA_IVA NUMERIC(5,2) = 0.13

			
	SET @POS = 1		
	SET @CODICONTRATO = ''
	SET @@CODIPROVEE = LEFT(@CODIPROVEEDOR,6)

	SET @@ID_ZAFRA_ACTUAL = (SELECT ID_ZAFRA FROM ZAFRA WHERE ACTIVA = 1)
	SET @@REND_HISTO = (SELECT dbo.fDividir2Dec(sum(A.AZUCAR_CATORCENA_REAL),sum(B.NETOTONEL)) 
						FROM ENVIO E, ANALISIS A, BASCULA B
						WHERE E.CERRADO = 1 AND NOT A.AZUCAR_CATORCENA_REAL IS NULL
						AND E.ID_ENVIO = A.ID_ENVIO AND E.ID_ENVIO = B.ID_ENVIO
						AND E.ID_ZAFRA = @@ID_ZAFRA_ACTUAL
						AND (	SELECT COUNT(1) 
								FROM CONTRATO T, LOTES L 
								WHERE T.CODICONTRATO = L.CODICONTRATO AND L.ACCESLOTE = E.ACCESLOTE
								AND T.CODIPROVEEDOR = @CODIPROVEEDOR
							) > 0
						)
	IF @@REND_HISTO IS NULL 
		BEGIN
			SET @@REND_HISTO = (SELECT REND_MODFINAN FROM ZAFRA WHERE ID_ZAFRA = @@ID_ZAFRA_ACTUAL)
		END
				
	INSERT INTO @TABLA_CONTRATOS
	SELECT DISTINCT CZ.ID_ZAFRA, C.CODICONTRATO, C.NO_CONTRATO 
	FROM CONTRATO C, LOTES L, CONTRATO_ZAFRAS CZ
	WHERE C.CODICONTRATO = L.CODICONTRATO AND C.CODICONTRATO = CZ.CODICONTRATO
	AND C.CODIPROVEEDOR = @CODIPROVEEDOR AND CZ.ID_ZAFRA BETWEEN @ID_ZAFRA_INI AND @ID_ZAFRA_FIN

	-- Obteniendo números de contrato
	set @NO_CONTRATOS = ''
	set @TOTAL_MZ = 0
	set @TOTAL_TC = 0
	set @TOTAL_MZZ2 = 0
	set @TOTAL_TCZ2 = 0
	declare curc1 cursor for select distinct NO_CONTRATO from @TABLA_CONTRATOS order by NO_CONTRATO
	open curc1 fetch next from curc1 into @CUR_NO_CONTRATO

	while @@FETCH_STATUS = 0
		begin						
		set @NO_CONTRATOS = @NO_CONTRATOS + cast(@CUR_NO_CONTRATO as varchar) + ','										

		fetch next from curc1 into @CUR_NO_CONTRATO
		end
	close curc1
	deallocate curc1		
	set @NO_CONTRATOS = LEFT(@NO_CONTRATOS,LEN(@NO_CONTRATOS)-1)
		
	-- Obteniendo zonas
	set @ZONAS = ''
	declare curc2 cursor for select distinct L.ZONA from LOTES L where (select count(1) from @TABLA_CONTRATOS C where C.CODICONTRATO = L.CODICONTRATO) > 0 order by ZONA
	open curc2 fetch next from curc2 into @CUR_ZONA
	while @@FETCH_STATUS = 0				
		begin
				
		set @ZONAS = @ZONAS + @CUR_ZONA + ','				
		fetch next from curc2 into @CUR_ZONA
		end
	close curc2
	deallocate curc2
	set @ZONAS = LEFT(@ZONAS,LEN(@ZONAS)-1)


	CREATE TABLE #Z_COSECHA(
		ID_ZAFRA INT,
		EN_COSECHA BIT
	)
	CREATE TABLE #Z_RENDS(
		ID_ZAFRA INT,
		REND NUMERIC(20,2)
	)
	CREATE TABLE #DETA(
		ID_ZAFRA INT,
		ORDEN INT,
		VALOR NUMERIC(20,10) -- NUMERIC(20,4)
	)

	/* Asignando valores de rendimientos */
	INSERT INTO #Z_RENDS VALUES( @@ID_ZAFRA_ACTUAL, @@REND_HISTO)
	INSERT #Z_RENDS
	SELECT ID_ZAFRA, REND_MODFINAN FROM ZAFRA WHERE ID_ZAFRA >= @ID_ZAFRA_INI AND ID_ZAFRA <= @ID_ZAFRA_FIN
	AND ID_ZAFRA <> @@ID_ZAFRA_ACTUAL
	/* --------------------------------  */

	INSERT #Z_COSECHA
	SELECT DISTINCT LC.ID_ZAFRA, 1
	FROM LOTES L, LOTES_COSECHA LC 
	WHERE L.ACCESLOTE = LC.ACCESLOTE 
	AND LC.ID_ZAFRA BETWEEN @ID_ZAFRA_INI AND @ID_ZAFRA_FIN 
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0 

	INSERT #Z_COSECHA
	SELECT DISTINCT CZ.ID_ZAFRA, 0
	FROM LOTES L, CONTRATO_ZAFRAS CZ
	WHERE L.CODICONTRATO = CZ.CODICONTRATO
	AND CZ.ID_ZAFRA BETWEEN @ID_ZAFRA_INI AND @ID_ZAFRA_FIN 
	AND (SELECT COUNT(1) FROM #Z_COSECHA Z WHERE Z.ID_ZAFRA = CZ.ID_ZAFRA) = 0 
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0 

	-- TOTAL AREA CULTIVADA (EN COSECHA)
	INSERT #DETA
	SELECT LC.ID_ZAFRA, 1, SUM(LC.MZ_ENTREGAR) AS MZ_ENTREGAR
	FROM LOTES L, LOTES_COSECHA LC
	WHERE L.ACCESLOTE = LC.ACCESLOTE
	AND (SELECT COUNT(1) FROM #Z_COSECHA Z WHERE Z.ID_ZAFRA = LC.ID_ZAFRA AND Z.EN_COSECHA = 1) > 0
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0
	GROUP BY LC.ID_ZAFRA
	-- AREA CULTIVADA PROPIA (EN COSECHA)
	UNION ALL
	SELECT LC.ID_ZAFRA, 2, SUM(LC.MZ_ENTREGAR) AS MZ_ENTREGAR
	FROM LOTES L, LOTES_COSECHA LC
	WHERE L.ACCESLOTE = LC.ACCESLOTE
	AND L.TIPO_DERECHO = 1
	AND (SELECT COUNT(1) FROM #Z_COSECHA Z WHERE Z.ID_ZAFRA = LC.ID_ZAFRA AND Z.EN_COSECHA = 1) > 0
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0
	GROUP BY LC.ID_ZAFRA
	-- AREA CULTIVADA ARRENDADA (EN COSECHA)
	UNION ALL
	SELECT LC.ID_ZAFRA, 3, SUM(LC.MZ_ENTREGAR) AS MZ_ENTREGAR
	FROM LOTES L, LOTES_COSECHA LC
	WHERE L.ACCESLOTE = LC.ACCESLOTE
	AND L.TIPO_DERECHO <> 1
	AND (SELECT COUNT(1) FROM #Z_COSECHA Z WHERE Z.ID_ZAFRA = LC.ID_ZAFRA AND Z.EN_COSECHA = 1) > 0
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0
	GROUP BY LC.ID_ZAFRA
	-- TC/MZ (EN COSECHA)
	UNION ALL
	SELECT LC.ID_ZAFRA, 4, dbo.fDividir2Dec(SUM(LC.TONEL_ENTREGAR),SUM(LC.MZ_ENTREGAR)) AS MZ_ENTREGAR
	FROM LOTES L, LOTES_COSECHA LC
	WHERE L.ACCESLOTE = LC.ACCESLOTE
	AND (SELECT COUNT(1) FROM #Z_COSECHA Z WHERE Z.ID_ZAFRA = LC.ID_ZAFRA AND Z.EN_COSECHA = 1) > 0
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0
	GROUP BY LC.ID_ZAFRA
	-- TOTAL TC (EN COSECHA)
	UNION ALL
	SELECT LC.ID_ZAFRA, 5, SUM(LC.TONEL_ENTREGAR) AS MZ_ENTREGAR
	FROM LOTES L, LOTES_COSECHA LC
	WHERE L.ACCESLOTE = LC.ACCESLOTE
	AND (SELECT COUNT(1) FROM #Z_COSECHA Z WHERE Z.ID_ZAFRA = LC.ID_ZAFRA AND Z.EN_COSECHA = 1) > 0
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0
	GROUP BY LC.ID_ZAFRA
	-- LBS AZUCAR/TC (EN COSECHA)
	UNION ALL	
	SELECT C.ID_ZAFRA, 6, (SELECT R.REND FROM #Z_RENDS R WHERE R.ID_ZAFRA = C.ID_ZAFRA) FROM #Z_COSECHA C WHERE C.EN_COSECHA = 1
	-- TOTAL LBS AZUCAR (EN COSECHA)
	UNION ALL
	SELECT LC.ID_ZAFRA, 7, SUM(LC.TONEL_ENTREGAR)* R.REND AS MZ_ENTREGAR
	FROM LOTES L, LOTES_COSECHA LC, #Z_RENDS R
	WHERE L.ACCESLOTE = LC.ACCESLOTE
	AND LC.ID_ZAFRA = R.ID_ZAFRA
	AND (SELECT COUNT(1) FROM #Z_COSECHA Z WHERE Z.ID_ZAFRA = LC.ID_ZAFRA AND Z.EN_COSECHA = 1) > 0
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0
	GROUP BY LC.ID_ZAFRA, R.REND
	-- VIP	
	INSERT #DETA
	SELECT C.ID_ZAFRA, 8, (SELECT Z.PRECIO_LIBRA_AZUCAR FROM ZAFRA Z WHERE Z.ID_ZAFRA = C.ID_ZAFRA) FROM #Z_COSECHA C WHERE C.EN_COSECHA = 1	
	-- TOTAL AREA CULTIVADA (CONTRATO)
	INSERT #DETA
	SELECT CZ.ID_ZAFRA, 1, SUM(L.AREA) AS MZ_ENTREGAR
	FROM LOTES L, CONTRATO_ZAFRAS CZ
	WHERE L.CODICONTRATO = CZ.CODICONTRATO
	AND (SELECT COUNT(1) FROM #Z_COSECHA Z WHERE Z.ID_ZAFRA = CZ.ID_ZAFRA AND Z.EN_COSECHA = 0) > 0
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0
	GROUP BY CZ.ID_ZAFRA
	-- AREA CULTIVADA PROPIA (CONTRATO)
	UNION ALL
	SELECT CZ.ID_ZAFRA, 2, SUM(L.AREA) AS MZ_ENTREGAR
	FROM LOTES L, CONTRATO_ZAFRAS CZ
	WHERE L.CODICONTRATO = CZ.CODICONTRATO
	AND L.TIPO_DERECHO = 1
	AND (SELECT COUNT(1) FROM #Z_COSECHA Z WHERE Z.ID_ZAFRA = CZ.ID_ZAFRA AND Z.EN_COSECHA = 0) > 0
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0
	GROUP BY CZ.ID_ZAFRA
	-- AREA CULTIVADA ARRENDADA (CONTRATO)
	UNION ALL
	SELECT CZ.ID_ZAFRA, 2, SUM(L.AREA) AS MZ_ENTREGAR
	FROM LOTES L, CONTRATO_ZAFRAS CZ
	WHERE L.CODICONTRATO = CZ.CODICONTRATO
	AND L.TIPO_DERECHO <> 1
	AND (SELECT COUNT(1) FROM #Z_COSECHA Z WHERE Z.ID_ZAFRA = CZ.ID_ZAFRA AND Z.EN_COSECHA = 0) > 0
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0
	GROUP BY CZ.ID_ZAFRA
	-- TC/MZ (CONTRATO)
	UNION ALL
	SELECT CZ.ID_ZAFRA, 4, AVG(L.TONELADAS) AS MZ_ENTREGAR
	FROM LOTES L, CONTRATO_ZAFRAS CZ
	WHERE L.CODICONTRATO = CZ.CODICONTRATO
	AND (SELECT COUNT(1) FROM #Z_COSECHA Z WHERE Z.ID_ZAFRA = CZ.ID_ZAFRA AND Z.EN_COSECHA = 0) > 0
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0
	GROUP BY CZ.ID_ZAFRA
	-- TOTAL TC (CONTRATO)
	UNION ALL
	SELECT CZ.ID_ZAFRA, 5, SUM(L.TONEL_TC) AS MZ_ENTREGAR
	FROM LOTES L, CONTRATO_ZAFRAS CZ
	WHERE L.CODICONTRATO = CZ.CODICONTRATO
	AND (SELECT COUNT(1) FROM #Z_COSECHA Z WHERE Z.ID_ZAFRA = CZ.ID_ZAFRA AND Z.EN_COSECHA = 0) > 0
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0
	GROUP BY CZ.ID_ZAFRA
	-- LBS AZUCAR/TC (EN COSECHA)
	UNION ALL
	SELECT C.ID_ZAFRA, 6, (SELECT R.REND FROM #Z_RENDS R WHERE R.ID_ZAFRA = C.ID_ZAFRA) FROM #Z_COSECHA C WHERE C.EN_COSECHA = 0
	-- TOTAL LBS AZUCAR (EN COSECHA)
	UNION ALL
	SELECT CZ.ID_ZAFRA, 7, SUM(L.TONEL_TC)* R.REND AS MZ_ENTREGAR
	FROM LOTES L, CONTRATO_ZAFRAS CZ, #Z_RENDS R
	WHERE L.CODICONTRATO = CZ.CODICONTRATO
	AND CZ.ID_ZAFRA = R.ID_ZAFRA
	AND (SELECT COUNT(1) FROM #Z_COSECHA Z WHERE Z.ID_ZAFRA = CZ.ID_ZAFRA AND Z.EN_COSECHA = 0) > 0
	AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO ) > 0
	GROUP BY CZ.ID_ZAFRA, R.REND
	-- VIP
	INSERT #DETA
	SELECT C.ID_ZAFRA, 8, (SELECT Z.PRECIO_LIBRA_AZUCAR FROM ZAFRA Z WHERE Z.ID_ZAFRA = C.ID_ZAFRA) FROM #Z_COSECHA C WHERE C.EN_COSECHA = 0
	-- =====================================================================================
	-- INGRESOS PROYECTADOS
	INSERT #DETA
	SELECT D.ID_ZAFRA, 9, ROUND(VALOR * (SELECT Z.PRECIO_LIBRA_AZUCAR FROM ZAFRA Z WHERE Z.ID_ZAFRA = D.ID_ZAFRA),2)
	FROM #DETA D
	WHERE D.ORDEN = 7
	
	-- =====================================================================================
	-- CAT (CORTE)
	INSERT #DETA
	SELECT D.ID_ZAFRA, 11, ROUND(VALOR * (SELECT Z.TARIFA_ROZA_MODFINAN + ROUND(Z.TARIFA_ROZA_MODFINAN * @TASA_IVA ,2) FROM ZAFRA Z WHERE Z.ID_ZAFRA = D.ID_ZAFRA),2)
	FROM #DETA D
	WHERE D.ORDEN = 5
	-- CAT (ALZA)
	INSERT #DETA
	SELECT D.ID_ZAFRA, 12, ROUND(VALOR * (SELECT Z.TARIFA_ALZA_MODFINAN + ROUND(Z.TARIFA_ALZA_MODFINAN * @TASA_IVA ,2) FROM ZAFRA Z WHERE Z.ID_ZAFRA = D.ID_ZAFRA),2)
	FROM #DETA D
	WHERE D.ORDEN = 5
	-- CAT (TRANSPORTE)
	INSERT #DETA
	SELECT D.ID_ZAFRA, 13, ROUND(VALOR * 
				(	SELECT CAST(ISNULL(AVG(LC.TARIFA_CORTA),0) AS NUMERIC(20,2)) + ROUND(CAST(ISNULL(AVG(LC.TARIFA_CORTA),0) AS NUMERIC(20,2)) * @TASA_IVA ,2) 
					FROM LOTES L, LOTES_COSECHA LC 
					WHERE NOT LC.TARIFA_CORTA IS NULL 
					AND L.ACCESLOTE = LC.ACCESLOTE 
					AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE L.CODICONTRATO = C.CODICONTRATO) > 0
				)
			,2)
	FROM #DETA D
	WHERE D.ORDEN = 5 AND (SELECT COUNT(1) FROM #Z_COSECHA C WHERE C.ID_ZAFRA = D.ID_ZAFRA AND C.EN_COSECHA = 1) > 0
	UNION ALL	
	SELECT D.ID_ZAFRA, 13, ROUND(VALOR * 
				(	
					SELECT AVG(TARIFA_AJUSTADA) + ROUND(AVG(TARIFA_AJUSTADA) * @TASA_IVA ,2)
					FROM (
						SELECT  (SELECT K.TARIFA_AJUSTADA FROM TARIFA_TRANS_X_KILO K WHERE N.KILOMETROS BETWEEN K.KILO_INI AND K.KILO_FIN)	AS TARIFA_AJUSTADA								    								
						FROM LOTES L, MAESTRO_LOTES ML, CANTON N
						WHERE L.ID_MAESTRO = ML.ID_MAESTRO		
						AND ML.CODI_DEPTO = N.CODI_DEPTO AND ML.CODI_MUNI = N.CODI_MUNI AND ML.CODI_CANTON = N.CODI_CANTON														
						AND (SELECT COUNT(1) FROM @TABLA_CONTRATOS C WHERE C.CODICONTRATO = L.CODICONTRATO) > 0 	
					) T															
				)
			,2)
	FROM #DETA D
	WHERE D.ORDEN = 5 AND (SELECT COUNT(1) FROM #Z_COSECHA C WHERE C.ID_ZAFRA = D.ID_ZAFRA AND C.EN_COSECHA = 0) > 0


	SET @TARIFA_FLETE = ISNULL(	(	SELECT AVG(TARIFA_AJUSTADA) + ROUND(AVG(TARIFA_AJUSTADA) * @TASA_IVA ,2)
									FROM (
